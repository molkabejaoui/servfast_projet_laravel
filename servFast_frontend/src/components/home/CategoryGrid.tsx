interface Category {
  id: number;
  name: string;
  description?: string;
  icon?: string;
  type: 'Solutions' | 'Enterprise';
  services_count?: number;
}

export default function CategoryGrid() {
  const navigate = useNavigate();
  const [categories, setCategories] = useState<Category[]>([]);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    api.get('/categories')
      .then((res) => setCategories(res.data))
      .catch((err) => console.error('Erreur catégories:', err))
      .finally(() => setLoading(false));
  }, []);

  if (loading) {
    return (
      <section className="px-16 py-12">
        <h2 className="text-2xl font-bold text-gray-900 mb-6">Browse by Category</h2>
        <div className="grid grid-cols-6 gap-4">
          {Array.from({ length: 6 }).map((_, i) => (
            <div key={i} className="border border-gray-100 rounded-2xl py-5 px-4 animate-pulse bg-gray-50 h-24" />
          ))}
        </div>
      </section>
    );
  }

  return (
    <section className="px-16 py-12">
      <h2 className="text-2xl font-bold text-gray-900 mb-6">Browse by Category</h2>
      <div className="grid grid-cols-6 gap-4">
        {categories.map((cat) => (
          <button
            key={cat.id}
            onClick={() => navigate(`/services?category_id=${cat.id}`)}
            className="border border-gray-100 rounded-2xl py-5 px-4 text-center no-underline text-inherit cursor-pointer transition-all hover:border-red-700 hover:bg-red-50 hover:-translate-y-1 hover:shadow-md flex flex-col items-center bg-transparent"
          >
            <div className="mb-2">{getCategoryIcon(cat.name)}</div>
            <div className="font-bold text-sm text-gray-900 mb-1">{cat.name}</div>
            {cat.services_count !== undefined && (
              <div className="text-xs text-gray-400">{cat.services_count} services</div>
            )}
          </button>
        ))}
      </div>
    </section>
  );
}
